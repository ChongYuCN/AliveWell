    //客户端检查
    public boolean chectMods() {
        Collection<ModContainer> mods = loader.getAllMods();

        for (ModContainer mod : mods) {
            String name = mod.getMetadata().getId();
            names.add(name);
            if(AliveAndWellMain.canCreative){
                AliveAndWellMain.LOGGER.info(name +"==================================");
            }
        }

        if(AliveAndWellMain.VERSION.contains("-modrinth")){
            return modCount(names)
                    && modrinth(names)
                    && modFTBModServer(names)//额外模组：FTB任务
                    && FabricLoader.getInstance().getModContainer("doom").get().getMetadata().getVersion().toString().equals("4.2.28")
                    && FabricLoader.getInstance().getModContainer("darkfear").get().getMetadata().getVersion().toString().equals("1.0.3");
        }else {
            return modCount(names)
                    && normal(names)
                    && FabricLoader.getInstance().getModContainer("doom").get().getMetadata().getVersion().toString().equals("4.2.28")
                    && FabricLoader.getInstance().getModContainer("darkfear").get().getMetadata().getVersion().toString().equals("1.0.3");
        }
    }

    //服务端检查
    public boolean chectModsServer() {
        Collection<ModContainer> mods = loader.getAllMods();

        for (ModContainer mod : mods) {
            String name = mod.getMetadata().getId();
            namesServer.add(name);
            if(AliveAndWellMain.canCreative){
                AliveAndWellMain.LOGGER.info(name +"###########################");
            }
        }

        if(AliveAndWellMain.VERSION.contains("-modrinth")){
            return modCountServer(namesServer)//模组数量
                    && modrinthServer(namesServer)//基础模组
                    && modFTBModServer(namesServer)//额外模组：FTB任务
                    && FabricLoader.getInstance().getModContainer("doom").get().getMetadata().getVersion().toString().equals("4.2.28")
                    && FabricLoader.getInstance().getModContainer("darkfear").get().getMetadata().getVersion().toString().equals("1.0.3");
        }else {
            return modCountServer(namesServer)//模组数量
                    && server(namesServer)
                    && FabricLoader.getInstance().getModContainer("doom").get().getMetadata().getVersion().toString().equals("4.2.28")
                    && FabricLoader.getInstance().getModContainer("darkfear").get().getMetadata().getVersion().toString().equals("1.0.3");
        }
    }

    //服务端加载模组数量
    public boolean modCount(Collection<String> names){
        if(AliveAndWellMain.VERSION.contains("-modrinth")){
            int modsFTB;
            if(CommonConfig.canAddFTBMod){
                modsFTB = 4;
            }else {
                modsFTB = 0;
            }
            return  names.size() == 313  + modsFTB ;
        }else {
            //标准版模组数量
            return names.size() == 354;
        }
    }

    //服务端加载模组数量
    public boolean modCountServer(Collection<String> names){
         if(AliveAndWellMain.VERSION.contains("-modrinth")){
            int modsFTB;
            if(CommonConfig.canAddFTBMod){
                modsFTB = 4;
            }else {
                modsFTB = 0;
            }
            return  names.size() == 261  + modsFTB ;
        }else {
            //标准版模组数量
            return names.size() == 292;
        }
    }

    //服务端加载模组：modrinth：FTB任务
    public boolean modFTBModServer(Collection<String> names){
        boolean has = names.contains("ftblibrary")
                && names.contains("ftbquests")
                && names.contains("ftbteams")
                && names.contains("itemfilters");

        if(CommonConfig.canAddFTBMod){
            if(has){
                return true;
            }else {
                return false;
            }
        }else {
            return !has;
        }
    }

